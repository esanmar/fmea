	// Export data in HTML/CSV/Word/Excel/Email/PDF format
	public function exportDocument($doc, $recordset, $startRec = 1, $stopRec = 1, $exportPageType = "")
	{
		if (!$recordset || !$doc)
			return;
		if (!$doc->ExportCustom) {

			// Write header
			$doc->exportTableHeader();
			if ($doc->Horizontal) { // Horizontal format, write header
				$doc->beginExportRow();
				if ($exportPageType == "view") {
					$doc->exportCaption($this->idProcess);
					$doc->exportCaption($this->process);
					$doc->exportCaption($this->regulation);
					$doc->exportCaption($this->qualification);
					$doc->exportCaption($this->supplierLevel2);
					$doc->exportCaption($this->supplierLevel3);
				} else {
					$doc->exportCaption($this->idProcess);
					$doc->exportCaption($this->process);
					$doc->exportCaption($this->regulation);
					$doc->exportCaption($this->qualification);
					$doc->exportCaption($this->supplierLevel2);
					$doc->exportCaption($this->supplierLevel3);
				}
				$doc->endExportRow();
			}
		}

		// Move to first record
		$recCnt = $startRec - 1;
		if (!$recordset->EOF) {
			$recordset->moveFirst();
			if ($startRec > 1)
				$recordset->move($startRec - 1);
		}
		while (!$recordset->EOF && $recCnt < $stopRec) {
			$recCnt++;
			if ($recCnt >= $startRec) {
				$rowCnt = $recCnt - $startRec + 1;

				// Page break
				if ($this->ExportPageBreakCount > 0) {
					if ($rowCnt > 1 && ($rowCnt - 1) % $this->ExportPageBreakCount == 0)
						$doc->exportPageBreak();
				}
				$this->loadListRowValues($recordset);

				// Render row
				$this->RowType = ROWTYPE_VIEW; // Render view
				$this->resetAttributes();
				$this->renderListRow();
				if (!$doc->ExportCustom) {
					$doc->beginExportRow($rowCnt); // Allow CSS styles if enabled
					if ($exportPageType == "view") {
						$doc->exportField($this->idProcess);
						$doc->exportField($this->process);
						$doc->exportField($this->regulation);
						$doc->exportField($this->qualification);
						$doc->exportField($this->supplierLevel2);
						$doc->exportField($this->supplierLevel3);
					} else {
						$doc->exportField($this->idProcess);
						$doc->exportField($this->process);
						$doc->exportField($this->regulation);
						$doc->exportField($this->qualification);
						$doc->exportField($this->supplierLevel2);
						$doc->exportField($this->supplierLevel3);
					}
					$doc->endExportRow($rowCnt);
				}
			}

			// Call Row Export server event
			if ($doc->ExportCustom)
				$this->Row_Export($recordset->fields);
			$recordset->moveNext();
		}
		if (!$doc->ExportCustom) {
			$doc->exportTableFooter();
		}
	}

