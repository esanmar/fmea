	// Validate form
	protected function validateForm()
	{
		global $Language, $FormError;

		// Initialize form error message
		$FormError = "";

		// Check if validation required
		if (!Config("SERVER_VALIDATE"))
			return ($FormError == "");
		if ($this->detection->Required) {
			if (!$this->detection->IsDetailKey && $this->detection->FormValue != NULL && $this->detection->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->detection->caption(), $this->detection->RequiredErrorMessage));
			}
		}
		if ($this->description->Required) {
			if (!$this->description->IsDetailKey && $this->description->FormValue != NULL && $this->description->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->description->caption(), $this->description->RequiredErrorMessage));
			}
		}
		if ($this->methods->Required) {
			if (!$this->methods->IsDetailKey && $this->methods->FormValue != NULL && $this->methods->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->methods->caption(), $this->methods->RequiredErrorMessage));
			}
		}
		if ($this->errorProofed->Required) {
			if ($this->errorProofed->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->errorProofed->caption(), $this->errorProofed->RequiredErrorMessage));
			}
		}
		if ($this->gonogo->Required) {
			if ($this->gonogo->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->gonogo->caption(), $this->gonogo->RequiredErrorMessage));
			}
		}
		if ($this->visualInspection->Required) {
			if ($this->visualInspection->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->visualInspection->caption(), $this->visualInspection->RequiredErrorMessage));
			}
		}
		if ($this->color->Required) {
			if (!$this->color->IsDetailKey && $this->color->FormValue != NULL && $this->color->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->color->caption(), $this->color->RequiredErrorMessage));
			}
		}

		// Return validate result
		$validateForm = ($FormError == "");

		// Call Form_CustomValidate event
		$formCustomError = "";
